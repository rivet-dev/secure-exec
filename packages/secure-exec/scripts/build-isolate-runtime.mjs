import * as esbuild from "esbuild";
import fs from "node:fs/promises";
import path from "node:path";

const runtimeSourceDir = path.join(process.cwd(), "isolate-runtime");
const runtimeDistDir = path.join(process.cwd(), "dist", "isolate-runtime");
const generatedManifestPath = path.join(
	process.cwd(),
	"src",
	"generated",
	"isolate-runtime.ts",
);

async function collectTypeScriptFiles(dir) {
	const entries = await fs.readdir(dir, { withFileTypes: true });
	const files = await Promise.all(
		entries.map(async (entry) => {
			const entryPath = path.join(dir, entry.name);
			if (entry.isDirectory()) {
				return collectTypeScriptFiles(entryPath);
			}
			if (entry.isFile() && entry.name.endsWith(".ts")) {
				return [entryPath];
			}
			return [];
		}),
	);
	return files.flat();
}

function toSourceId(filePath) {
	const relativePath = path
		.relative(runtimeSourceDir, filePath)
		.split(path.sep)
		.join("/");
	const noExt = relativePath.replace(/\.ts$/, "");
	return noExt.replace(/[^a-zA-Z0-9]+([a-zA-Z0-9])/g, (_, char) =>
		char.toUpperCase(),
	);
}

function createManifestSource(sourceEntries) {
	const header =
		"// Auto-generated by scripts/build-isolate-runtime.mjs. Do not edit manually.\n";
	const mapLines = Object.entries(sourceEntries)
		.map(([id, source]) => `\t${JSON.stringify(id)}: ${JSON.stringify(source)},`)
		.join("\n");

	return [
		header,
		"export const ISOLATE_RUNTIME_SOURCES = {",
		mapLines,
		"} as const;",
		"",
		"export type IsolateRuntimeSourceId = keyof typeof ISOLATE_RUNTIME_SOURCES;",
		"",
		"export function getIsolateRuntimeSource(id: IsolateRuntimeSourceId): string {",
		"\treturn ISOLATE_RUNTIME_SOURCES[id];",
		"}",
		"",
	].join("\n");
}

await fs.mkdir(runtimeDistDir, { recursive: true });
await fs.mkdir(path.dirname(generatedManifestPath), { recursive: true });

const sourceFiles = (await collectTypeScriptFiles(runtimeSourceDir)).sort();
const runtimeSources = {};

for (const sourceFile of sourceFiles) {
	const sourceId = toSourceId(sourceFile);
	const relativePath = path
		.relative(runtimeSourceDir, sourceFile)
		.split(path.sep)
		.join("/");
	const outputFile = path.join(
		runtimeDistDir,
		relativePath.replace(/\.ts$/, ".js"),
	);
	await fs.mkdir(path.dirname(outputFile), { recursive: true });

	const sourceText = await fs.readFile(sourceFile, "utf8");
	const transformed = await esbuild.transform(sourceText, {
		loader: "ts",
		format: "iife",
		target: "es2022",
		platform: "browser",
	});

	const compiledCode = transformed.code.trimEnd() + "\n";
	runtimeSources[sourceId] = compiledCode;
	await fs.writeFile(outputFile, compiledCode, "utf8");
}

await fs.writeFile(
	generatedManifestPath,
	createManifestSource(runtimeSources),
	"utf8",
);

console.log(
	`Wrote ${Object.keys(runtimeSources).length} isolate runtime sources to ${runtimeDistDir}`,
);
console.log(`Updated isolate runtime manifest at ${generatedManifestPath}`);
